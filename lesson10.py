# intersection (&) - віводить то, що спільне входит у єлементи
# symmetric_difference - входит абсолютно все єлмнт которые есть в одном из списков, но нет в обоих
# issubset - входит ли первій списко во сторой. типа {1,2,3} {1,2,3,4,5} вот тут 1,2,3 входит во второй список

